const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "border-2 rounded-xs bg-card text-card-foreground shadow-xs",
      className
    )}
    data-slot="card"
    {...props}
  />
));
Card.displayName = "Card";

